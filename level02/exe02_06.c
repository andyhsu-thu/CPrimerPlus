#include <stdio.h>
int main(void)
{
    int toes = 10;
    int toes_dbl, toes_squared;

    toes_dbl = 2 * toes;
    toes_squared = toes * toes;
    printf("toes = %d, toes_dbl = %d, toes_squared = %d\n", toes, 
    toes_dbl, toes_squared);

    return 0;
}